package com.matri.bean;

public class PhysicalAttributesBean {

	private int physicalAttributeId;

	private int heightFeet;

	private int heightInch;

	private int weight;

	private String complexion;

	private String spectacles;

	private String bloodGroup;

	private String physicalDisability;

	private String diet;

	public int getPhysicalAttributeId() {
		return physicalAttributeId;
	}

	public void setPhysicalAttributeId(int physicalAttributeId) {
		this.physicalAttributeId = physicalAttributeId;
	}

	public int getHeightFeet() {
		return heightFeet;
	}

	public void setHeightFeet(int heightFeet) {
		this.heightFeet = heightFeet;
	}

	public int getHeightInch() {
		return heightInch;
	}

	public void setHeightInch(int heightInch) {
		this.heightInch = heightInch;
	}

	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}

	public String getComplexion() {
		return complexion;
	}

	public void setComplexion(String complexion) {
		this.complexion = complexion;
	}

	public String getSpectacles() {
		return spectacles;
	}

	public void setSpectacles(String spectacles) {
		this.spectacles = spectacles;
	}

	public String getBloodGroup() {
		return bloodGroup;
	}

	public void setBloodGroup(String bloodGroup) {
		this.bloodGroup = bloodGroup;
	}

	public String getPhysicalDisability() {
		return physicalDisability;
	}

	public void setPhysicalDisability(String physicalDisability) {
		this.physicalDisability = physicalDisability;
	}

	public String getDiet() {
		return diet;
	}

	public void setDiet(String diet) {
		this.diet = diet;
	}

}
