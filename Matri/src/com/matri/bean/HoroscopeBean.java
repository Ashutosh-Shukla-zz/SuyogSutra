package com.matri.bean;

public class HoroscopeBean {

	private int horoscopeBeanId;

	private String caste;

	private String subCaste;

	private String nadi;

	private String gan;

	private String nakshtra;

	private String gotra;

	private String manglik;

	private String placeOfBirth;

	private String timeOfBirth;

	public int getHoroscopeBeanId() {
		return horoscopeBeanId;
	}

	public void setHoroscopeBeanId(int horoscopeBeanId) {
		this.horoscopeBeanId = horoscopeBeanId;
	}

	public String getCaste() {
		return caste;
	}

	public void setCaste(String caste) {
		this.caste = caste;
	}

	public String getSubCaste() {
		return subCaste;
	}

	public void setSubCaste(String subCaste) {
		this.subCaste = subCaste;
	}

	public String getNadi() {
		return nadi;
	}

	public void setNadi(String nadi) {
		this.nadi = nadi;
	}

	public String getGan() {
		return gan;
	}

	public void setGan(String gan) {
		this.gan = gan;
	}

	public String getNakshtra() {
		return nakshtra;
	}

	public void setNakshtra(String nakshtra) {
		this.nakshtra = nakshtra;
	}

	public String getGotra() {
		return gotra;
	}

	public void setGotra(String gotra) {
		this.gotra = gotra;
	}

	public String getManglik() {
		return manglik;
	}

	public void setManglik(String manglik) {
		this.manglik = manglik;
	}

	public String getPlaceOfBirth() {
		return placeOfBirth;
	}

	public void setPlaceOfBirth(String placeOfBirth) {
		this.placeOfBirth = placeOfBirth;
	}

	public String getTimeOfBirth() {
		return timeOfBirth;
	}

	public void setTimeOfBirth(String timeOfBirth) {
		this.timeOfBirth = timeOfBirth;
	}

}
