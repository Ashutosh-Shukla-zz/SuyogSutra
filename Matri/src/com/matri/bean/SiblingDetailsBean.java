package com.matri.bean;

public class SiblingDetailsBean {

	private String siblingDetailsId;

	private String fullName;

	private String occupation;

	private String maritalStatus;

	private String gender;

	private String spouseName;

	private String spouseOccupation;

	private String spouseNativeAddress;

	private String residentialAddress;

	public String getSiblingDetailsId() {
		return siblingDetailsId;
	}

	public void setSiblingDetailsId(String siblingDetailsId) {
		this.siblingDetailsId = siblingDetailsId;
	}

	public String getFullName() {
		return fullName;
	}

	public void setFullName(String fullName) {
		this.fullName = fullName;
	}

	public String getOccupation() {
		return occupation;
	}

	public void setOccupation(String occupation) {
		this.occupation = occupation;
	}

	public String getMaritalStatus() {
		return maritalStatus;
	}

	public void setMaritalStatus(String maritalStatus) {
		this.maritalStatus = maritalStatus;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getSpouseName() {
		return spouseName;
	}

	public void setSpouseName(String spouseName) {
		this.spouseName = spouseName;
	}

	public String getSpouseOccupation() {
		return spouseOccupation;
	}

	public void setSpouseOccupation(String spouseOccupation) {
		this.spouseOccupation = spouseOccupation;
	}

	public String getSpouseNativeAddress() {
		return spouseNativeAddress;
	}

	public void setSpouseNativeAddress(String spouseNativeAddress) {
		this.spouseNativeAddress = spouseNativeAddress;
	}

	public String getResidentialAddress() {
		return residentialAddress;
	}

	public void setResidentialAddress(String residentialAddress) {
		this.residentialAddress = residentialAddress;
	}

}
